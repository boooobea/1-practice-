package Switch;

public class SwitchStringExample {
	public static void main(String[] args) {
		double number = 3.14159;
		
		switch(number) {
		case 1.2:
			System.out.println("1.2");
			break;
		case 2.3:
			System.out.println("2.3");
			break;
		case 3.14:
			System.out.println("3.14");
		default :
			System.out.println("NO");
		}//switch
		
	}//main

}//end class
